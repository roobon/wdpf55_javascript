console.log("lets try post increment")
let c = 10;
let d = c++; //post increment shows previous value. 
console.log(d);

console.log("lets try post decrement")
let e = 10;
let f = e--; //post decrement shows previous value. 
console.log(f);
console.log();

console.log("lets try pre increment")
let m = 10;
let n = ++m; //pre increment shows +1 value with previous value. 
console.log(n);

console.log("lets try pre decrement")
let x = 10;
let y = --x; //pre decrement shows -1 value with previous value. 
